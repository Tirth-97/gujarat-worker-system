def t(key: str, lang: str = "Gujarati", **kwargs) -> str:
    """
    Get a translation string.
    Falls back to English if key not found in selected language.
    Supports format kwargs like t("registration_success", ref_id="GJ-001")
    """
    text = TRANSLATIONS.get(lang, TRANSLATIONS["English"]).get(
        key,
        TRANSLATIONS["English"].get(key, key)
    )
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text
